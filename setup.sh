#!/usr/bin/env bash
# claude-code-backup — setup.sh
# First-run installer:
#   1. verifies gh auth
#   2. creates two PRIVATE GitHub data repos (Claude + OpenClaude)
#   3. writes ./.env with repo names + paths
#   4. invokes backup.sh once
#
# Usage:
#   ./setup.sh                                  # prompt for repo names
#   ./setup.sh claude-repo openclaude-repo      # names as args (no flag)

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

log()  { printf '[setup] %s\n' "$*"; }
fail() { printf '[setup][ERROR] %s\n' "$*" >&2; exit 1; }

GH_USER="$(gh api user --jq .login 2>/dev/null)" \
  || fail "gh CLI not authed. Run: gh auth login"

log "GitHub user: $GH_USER"

# default repo names — override with argv or prompts
DEFAULT_CLAUDE_REPO="$GH_USER/claude-config-backup"
DEFAULT_OPENCLAUDE_REPO="$GH_USER/openclaude-config-backup"

if [[ $# -ge 2 ]]; then
  CLAUDE_REPO="$1"
  OPENCLAUDE_REPO="$2"
else
  read -rp "Claude backup repo [$DEFAULT_CLAUDE_REPO]: " CLAUDE_REPO
  CLAUDE_REPO="${CLAUDE_REPO:-$DEFAULT_CLAUDE_REPO}"
  read -rp "OpenClaude backup repo [$DEFAULT_OPENCLAUDE_REPO]: " OPENCLAUDE_REPO
  OPENCLAUDE_REPO="${OPENCLAUDE_REPO:-$DEFAULT_OPENCLAUDE_REPO}"
fi

# normalize to owner/name
[[ "$CLAUDE_REPO" != */* ]]     && CLAUDE_REPO="$GH_USER/$CLAUDE_REPO"
[[ "$OPENCLAUDE_REPO" != */* ]] && OPENCLAUDE_REPO="$GH_USER/$OPENCLAUDE_REPO"

create_repo_if_missing() {
  local repo="$1" label="$2"
  log "checking $label repo: $repo"
  if gh repo view "$repo" >/dev/null 2>&1; then
    log "  exists"
    return
  fi
  log "  creating PRIVATE repo: $repo"
  gh repo create "$repo" --private --description "$label config backup" \
    || fail "failed to create $repo"
}

create_repo_if_missing "$CLAUDE_REPO"     "Claude"
create_repo_if_missing "$OPENCLAUDE_REPO" "OpenClaude"

log "writing .env"
cat > "$SCRIPT_DIR/.env" <<EOF
# Auto-generated by setup.sh on $(date -u +%Y-%m-%dT%H:%M:%SZ)
# These repos are PRIVATE — they contain OAuth tokens, API keys, and secrets.
CLAUDE_BACKUP_REPO=$CLAUDE_REPO
OPENCLAUDE_BACKUP_REPO=$OPENCLAUDE_REPO
CLAUDE_HOME=$HOME/.claude
OPENCLAUDE_HOME=$HOME/.openclaude
BACKUP_WORKDIR=$HOME/.claude-backup-work
EOF

# .env is git-ignored at the scripts repo level (see .gitignore)
chmod 600 "$SCRIPT_DIR/.env" 2>/dev/null || true

log "running first backup"
bash "$SCRIPT_DIR/backup.sh"

log "setup complete."
log "next: schedule automatic backups — see README.md 'Scheduling' section."
